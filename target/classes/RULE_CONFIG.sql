CREATE TABLE `test`.`RULE_CONFIG` (
  `CONFIG_ID` INT NOT NULL AUTO_INCREMENT,
  `CONFIG_RULE_ID` INT NOT NULL,
  `CONFIG_NAME` VARCHAR(255) NOT NULL,
  `CONFIG_VALUE` VARCHAR(255) NOT NULL,
  PRIMARY KEY (`CONFIG_ID`),
  UNIQUE INDEX `CONFIG_ID_UNIQUE` (`CONFIG_ID` ASC),
  INDEX `CONFIG_RULE_ID_FK_idx` (`CONFIG_RULE_ID` ASC),
  CONSTRAINT `CONFIG_RULE_ID_FK`
    FOREIGN KEY (`CONFIG_RULE_ID`)
    REFERENCES `test`.`RULE` (`RULE_ID`)
    ON DELETE NO ACTION
    ON UPDATE CASCADE);
